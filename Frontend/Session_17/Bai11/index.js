let input1 = parseFloat(prompt("Sử dụng hàm Math.abs()"));
document.write(Math.abs(input1)  + "<br>");//	Làm tròn n đến số nguyên gần nhất

let input2 = parseFloat(prompt("Sử dụng hàm Math.ceil()"));
document.write(Math.ceil(input2)  + "<br>"); // 	Làm tròn xuống số nguyên nhỏ hơn hoặc bằng n

let input3 = parseFloat(prompt("Sử dụng hàm Math.floor()"));
document.write(Math.floor(input3)  + "<br>"); //	Làm tròn lên số nguyên lớn hơn hoặc bằng n

let input4 = parseFloat(prompt("Sử dụng hàm Math.round()"));
document.write(Math.round(input4)  + "<br>");//	Lấy giá trị tuyệt đối của n

